package com.hawkprime.syndicate.util;

/**
 * Value Type Enumeration.
 */
public enum ValueType {
	/** String type. */
	STRING,

	/** Boolean type. */
	BOOLEAN,

	/** Short, Integer type. */
	INTEGER,

	/** Long type. */
	LONG,

	/** Double, Float type. */
	DECIMAL,

	/** Date, LocalDateTime type. */
	DATE
}
