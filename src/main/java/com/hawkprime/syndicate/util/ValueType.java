package com.hawkprime.syndicate.util;

/**
 * Value Type Enumeration.
 *
 * @version 5.0.0
 * @author Juan D Frias <juandfrias@gmail.com>
 */
public enum ValueType {
	/** String type. */
	STRING,

	/** Boolean type. */
	BOOLEAN,

	/** Short, Integer type. */
	INTEGER,

	/** Long type. */
	LONG,

	/** Double, Float type. */
	DECIMAL,

	/** Date, LocalDateTime type. */
	DATE
}
